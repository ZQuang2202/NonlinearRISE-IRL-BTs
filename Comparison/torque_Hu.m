function f = torque_Hu(t, k, tu)
    if k > 10000 && k <= 11000
        f = [-10; -10];
    elseif k > 11000 && k <= 11500
        f = [-10; 
               -10 + 5.1*(1 - exp(-10*t(k - 11000)))];
    elseif k > 11500 && k <= 12200
        f = [-10 + 2*(1 - exp(-5*t(k - 11500)));
                tu{11500}(2) + 1*(1 - exp(-10*t(k - 11500)))];
    elseif k > 12200 && k <= 13000
        f = [tu{12200}(1) + 1.5*(1 - exp(-10*t(k - 12200)));
                tu{12200}(2)];
    elseif k > 13000 && k <= 20000
        f = tu{13000};
    elseif k > 20000 && k <= 20200
        f = [10*exp(-3*t(k - 20000));
                10];
    elseif k > 20200 && k <= 20600
        f = tu{20150};
    elseif k > 20600 && k <= 22000
        f = [tu{20600}(1)*exp(-1*t(k - 20600));
                10];
    elseif k > 22000 && k <= 27000
    f = [tu{20600}(1)*exp(-1*t(k - 20600));
            tu{22000}(2)*exp(-1*t(k - 22000))];     
    else
        f = [0; 0];
    end
end



